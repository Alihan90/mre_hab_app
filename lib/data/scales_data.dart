// lib/data/scales_data.dart

enum ScaleType {
  yesNoList,      // Список питань Так/Ні (1 або 0 балів) - Рівермід
  singleChoice,   // Скринінг за рівнями / Оксфордська шкала - GMFCS, MMT, mRS
  multiRadioGroup // Кожне питання має свої унікальні варіанти - Бартел, Берг
}

class ScaleOption {
  final int score;
  final String text;
  const ScaleOption(this.score, this.text);
}

class ScaleQuestion {
  final String id;
  final String text;
  final List<ScaleOption> options; 
  const ScaleQuestion({required this.id, required this.text, this.options = const []});
}

class ScaleProtocol {
  final String id;
  final String name;
  final ScaleType type;
  final List<ScaleQuestion> questions;
  final List<ScaleOption> globalOptions; 
  final String Function(int totalScore)? interpreter;

  const ScaleProtocol({
    required this.id,
    required this.name,
    required this.type,
    this.questions = const [],
    this.globalOptions = const [],
    this.interpreter,
  });
}

